class SAT(nn.Module):
    def __init__(self, dim, input_resolution, num_heads, win_size, shift_size=0,
                 mlp_ratio=4., qkv_bias=True, qk_scale=None, drop=0., attn_drop=0., drop_path=0.,
                 act_layer=nn.GELU, norm_layer=nn.LayerNorm, token_projection='linear', token_mlp='ffn',
                 se_layer=False):
        super().__init__()
        self.dim = dim
        self.input_resolution = input_resolution
        self.num_heads = num_heads
        self.win_size = win_size
        self.shift_size = shift_size
        self.mlp_ratio = mlp_ratio
        self.token_mlp = token_mlp
        if min(self.input_resolution) <= self.win_size[0]:
            self.shift_size = 0
            self.win_size[0] = min(self.input_resolution)
        assert 0 <= self.shift_size < self.win_size[0], "shift_size must in 0-win_size"

        self.norm1 = norm_layer(dim)
        self.attn = WindowAttention_Horizontally(
            dim, win_size=self.win_size, num_heads=num_heads,
            qkv_bias=qkv_bias, qk_scale=qk_scale, attn_drop=attn_drop, proj_drop=drop,
            token_projection=token_projection, se_layer=se_layer)

        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        self.norm2 = norm_layer(dim)
        mlp_hidden_dim = int(dim * mlp_ratio)
        self.mlp = Mlp(in_features=dim, hidden_features=mlp_hidden_dim, act_layer=act_layer,
                       drop=drop) if token_mlp == 'ffn' else LeFF(dim, mlp_hidden_dim, act_layer=act_layer, drop=drop)


    def forward(self, query, key, value, x_size=(30, 90),):

        B, L, C = query.shape
        H, W = x_size
        shortcut = query
        query = self.norm1(query)
        key = self.norm1(key)
        value = self.norm1(value)

        q = query.view(B, H, W, C)
        k = key.view(B, H, W, C)
        v = value.view(B, H, W, C)

        # partition windows
        x_windows_query = window_partition_horizontally(q, self.win_size)  # nW*B, win_size, win_size, C  N*C->C
        x_windows_query = x_windows_query.view(-1, self.win_size[0] * self.win_size[1], C)  # nW*B, win_size*win_size, C

        x_windows_key = window_partition_horizontally(k, self.win_size)  # nW*B, win_size, win_size, C  N*C->C
        x_windows_key = x_windows_key.view(-1, self.win_size[0] * self.win_size[1], C)  # nW*B, win_size*win_size, C

        x_windows_value = window_partition_horizontally(v, self.win_size)  # nW*B, win_size, win_size, C  N*C->C
        x_windows_value = x_windows_value.view(-1, self.win_size[0] * self.win_size[1], C)  # nW*B, win_size*win_size, C

        attn_windows = self.attn(query=x_windows_query, key=x_windows_key, value=x_windows_value)  # nW*B, win_size*win_size, C

        attn_windows = attn_windows.view(-1, self.win_size[0], self.win_size[1], C)
        shifted_x = window_reverse_horizontally(attn_windows, self.win_size, H, W)  # B H' W' C

        x = shifted_x
        x = x.view(B, H * W, C)
        x = shortcut + self.drop_path(x)
        x = x + self.drop_path(self.mlp(self.norm2(x)))
        return x



class SFT(nn.Module):
    def __init__(self, dim, input_resolution, num_heads, mask=(1, 5), shift_size=0,
                 mlp_ratio=4., qkv_bias=True, qk_scale=None, drop=0., attn_drop=0., drop_path=0.,
                 act_layer=nn.GELU, norm_layer=nn.LayerNorm, token_projection='linear', token_mlp='ffn',
                 se_layer=False):
        super().__init__()
        self.dim = dim
        self.input_resolution = input_resolution
        self.num_heads = num_heads
        self.mask = mask
        self.shift_size = shift_size
        self.mlp_ratio = mlp_ratio
        self.token_mlp = token_mlp
        if min(self.input_resolution) <= self.mask[0]:
            self.shift_size = 0
            self.mask[0] = min(self.input_resolution)


        self.norm1 = norm_layer(dim)
        self.attn = WindowAttention_Horizontally(
            dim, win_size=self.mask, num_heads=num_heads,
            qkv_bias=qkv_bias, qk_scale=qk_scale, attn_drop=attn_drop, proj_drop=drop,
            token_projection=token_projection, se_layer=se_layer)

        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
        self.norm2 = norm_layer(dim)
        mlp_hidden_dim = int(dim * mlp_ratio)
        self.mlp = Mlp(in_features=dim, hidden_features=mlp_hidden_dim, act_layer=act_layer,
                       drop=drop) if token_mlp == 'ffn' else LeFF(dim, mlp_hidden_dim, act_layer=act_layer, drop=drop)

    def extra_repr(self) -> str:
        return f"dim={self.dim}, input_resolution={self.input_resolution}, num_heads={self.num_heads}, " \
               f"mask={self.mask[0]}, shift_size={self.shift_size}, mlp_ratio={self.mlp_ratio}"

    def forward(self, query, key, value, x_size=(16, 32), mask=None):

        B, L, C = query.shape
        H, W = x_size
        attn_mask = None
        shortcut = query
        query = self.norm1(query)
        key = self.norm1(key)
        value = self.norm1(value)

        q = query.view(B, H, W, C)
        k = key.view(B, H, W, C)
        v = value.view(B, H, W, C)

        # partition windows
        x_windows_query = window_partition_horizontally(q, self.mask)
        x_windows_query = x_windows_query.view(-1, self.mask[0] * self.mask[1], C)
        x_windows_key = window_partition_horizontally(k, self.mask)
        x_windows_key = x_windows_key.view(-1, self.mask[0] * self.mask[1], C)
        x_windows_value = window_partition_horizontally(v, self.mask)
        x_windows_value = x_windows_value.view(-1, self.mask[0] * self.mask[1], C)

        # W-MSA/SW-MSA
        attn_windows = self.attn(query=x_windows_query, key=x_windows_key, value=x_windows_value)

        # merge windows
        attn_windows = attn_windows.view(-1, self.mask[0], self.mask[1], C)
        shifted_x = window_reverse_horizontally(attn_windows, self.mask, H, W)

        x = shifted_x
        x = x.view(B, H * W, C)

        # FFN
        x = shortcut + self.drop_path(x)
        x = x + self.drop_path(self.mlp(self.norm2(x)))
        del attn_mask
        return x

    def flops(self):
        flops = 0
        H, W = self.input_resolution
        # norm1
        flops += self.dim * H * W
        # W-MSA/SW-MSA
        flops += self.attn.flops(H, W)
        # norm2
        flops += self.dim * H * W
        # mlp
        flops += self.mlp.flops(H, W)
        print("LeWin:{%.2f}" % (flops / 1e9))
        return flops

